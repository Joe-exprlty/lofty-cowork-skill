// Build the email signature block from owner env vars. Phone, email,
// and website lines render only when the corresponding var is set.
function buildSignatureHtml(owner) {
  const parts = [];
  parts.push('<p style="margin:24px 0 16px 0;color:#555;">If anything changed or you want to add to this, just reply to this email.</p>');
  parts.push('<p style="margin:0;font-weight:600;color:#222;">' + escapeHtml(owner.fullName) + '</p>');
  if (owner.brokerage) {
    parts.push('<p style="margin:0;color:#555;">' + escapeHtml(owner.brokerage) + '</p>');
  }
  if (owner.phone) {
    const phoneDigits = owner.phone.replace(/[^0-9+]/g, "");
    parts.push('<p style="margin:0;"><a href="tel:' + phoneDigits + '" style="color:#2c5282;text-decoration:none;">' + escapeHtml(owner.phone) + '</a></p>');
  }
  if (owner.email) {
    parts.push('<p style="margin:0;"><a href="mailto:' + escapeHtml(owner.email) + '" style="color:#2c5282;text-decoration:none;">' + escapeHtml(owner.email) + '</a></p>');
  }
  if (owner.website) {
    const display = owner.website.replace(/^https?:\/\//, "").replace(/^www\./, "").replace(/\/$/, "");
    const href = owner.website.startsWith("http") ? owner.website : "https://" + owner.website;
    parts.push('<p style="margin:2px 0 0 0;"><a href="' + escapeHtml(href) + '" style="color:#2c5282;text-decoration:none;">' + escapeHtml(display) + '</a></p>');
  }
  return parts.join("");
}

function tagListHtml(tags, color) {
  const chips = tags
    .map(
      (t) =>
        '<span style="display:inline-block;margin:2px 6px 2px 0;padding:4px 10px;background:' +
        color +
        '15;color:' +
        color +
        ';border:1px solid ' +
        color +
        '40;border-radius:999px;font-size:13px;">' +
        escapeHtml(t) +
        "</span>"
    )
    .join("");
  return '<div style="margin:0;">' + chips + "</div>";
}

