  const ratingRows = [];
  RATING_FIELDS.forEach((f) => {
    const n = parseRating(submission, f.aliases, f.column, purposeToQid);
    if (n !== null) {
      const label = f.label === "Does this make the short list" ? "Does this make your short list" : f.label;
      ratingRows.push(label + ": " + n + " of 5 stars");
    }
  });

  const standoutF = fieldByColumn(TEXT_FIELDS, "standout_text");
  const memoryF = fieldByColumn(TEXT_FIELDS, "memory_notes");
  const lovedF = fieldByColumn(TAG_FIELDS, "loved_tags");
